    {
        $coupon = Coupon::where('code', $request->coupon_code)->first();

        if(!$coupon)
        {
            Toastr::error('Invalid coupon number!', 'Access denied!');
            return redirect()->route('checkout.index');
        }
        
        session()->put('coupon', [
            'name' => $coupon->code,
            'discount' => discount($coupon, Cart::subtotal()),
        ]);
        
